    {
        return $this->render('about');
    }
    /**
    * Displays Projects page
    * Displays all projects too
    * @return mixed
    */
    public function actionProjects()
    {
        $query = Projects::find()->all();
        return $this->render('projects/data', ['Projects' => $query]);

    }

    /**
    * Displays Projects page
    * Displays all projects too
